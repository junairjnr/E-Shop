import Navbar from '../Components/Navbar';
import ProductCard from '../card/page';
import { useState } from 'react';
import useCartStore from '../store';


export default function Dashboard() {
    const [searchTerm, setSearchTerm] = useState('');
    const [filters, setFilters] = useState({
        gender: '',
        color: '',
        price: '',
        type: ''
    });

    const filteredProducts = products?.filter((product) => {
        return (
            product.name.toLowerCase().includes(searchTerm.toLowerCase()) ||
            product.color.toLowerCase().includes(searchTerm.toLowerCase()) ||
            product.type.toLowerCase().includes(searchTerm.toLowerCase())
        );
    }).filter(product => {
        const genderMatch = filters.gender ? product.gender === filters.gender : true;
        const colorMatch = filters.color ? product.color === filters.color : true;
        const priceMatch = filters.price ? product.price <= filters.price : true;
        const typeMatch = filters.type ? product.type === filters.type : true;
        return genderMatch && colorMatch && priceMatch && typeMatch;
    });


    const { cart } = useCartStore()
    const cartCount = cart.length;

    return (
        <div className='w-full h-full'>
            <Navbar cartCount={cartCount} />
            <div className="h-full w-full max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 mb-5">
                <div className='w-full h-full shadow-lg p-2'>
                    {/* Search Input */}
                    <input
                        type="text"
                        className="border border-gray-300 rounded-lg px-4 py-2 mb-4 w-full"
                        placeholder="Search by name, color, or type..."
                        value={searchTerm}
                        onChange={(e) => setSearchTerm(e.target.value)}
                    />

                    {/* Filter Section */}
                    <div className="mb-4 flex space-x-4 md:flex-row flex-col gap-3">
                        <select className="border border-gray-300 rounded-lg px-4 py-2"
                            onChange={(e) => setFilters({ ...filters, gender: e.target.value })}>
                            <option value="">Filter by Gender</option>
                            <option value="Men">Men</option>
                            <option value="Women">Women</option>
                        </select>

                        <select className="border border-gray-300 rounded-lg px-4 py-2"
                            onChange={(e) => setFilters({ ...filters, color: e.target.value })}>
                            <option value="">Filter by Color</option>
                            <option value="Black">Black</option>
                            <option value="Blue">Blue</option>
                            <option value="Red">Red</option>
                            <option value="Grey">Grey</option>
                            <option value="Green">Green</option>
                            <option value="Yellow">Yellow</option>
                            <option value="Purple">Purple</option>
                            <option value="Pink">Pink</option>
                            {/* Add more colors */}
                        </select>

                        <select className="border border-gray-300 rounded-lg px-4 py-2"
                            onChange={(e) => setFilters({ ...filters, price: e.target.value })}>
                            <option value="">Filter by Price</option>
                            <option value="300">Under ₹300</option>
                            <option value="500">Under ₹500</option>
                        </select>

                        <select className="border border-gray-300 rounded-lg px-4 py-2"
                            onChange={(e) => setFilters({ ...filters, type: e.target.value })}>
                            <option value="">Filter by Type</option>
                            <option value="Polo">Polo</option>
                            <option value="Hoodie">Hoodie</option>
                            <option value="Basic">Basic</option>
                        </select>
                    </div>


                </div>

                {/* Product Grid */}
                {filteredProducts.length > 0 ? (
                    <div className='w-full h-full rounded-md'>
                        <div className="grid grid-cols-1 sm:grid-cols-2 md:grid-cols-3 lg:grid-cols-4 gap-8 mt-6 ">
                            {filteredProducts.map((product) => (
                                <ProductCard key={product.id} product={product} />
                            ))}
                        </div>
                    </div>
                ) : (
                    <div className='w-full h-screen flex justify-center py-28'>
                        <p className="text-center font-bold text-2xl">Oops! No items found in your search.</p>
                    </div>
                )}
            </div>
        </div>
    );
}

